//In this array we are printing all the values instead of one value, we use loop for this!
public class Print {
    public static void main(String[] args) 
    {
        int num[]= new int[4];
        num[0] = 5;
        num[1] = 6;
        num[2] = 2;
        num[3] = 8;

        for(int i=0; i<4; i++)
        {
        System.out.println(num[i]);
        }
    }
} 
    
